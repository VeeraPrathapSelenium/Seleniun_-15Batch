package Strings;

public class ASCIIandCharacters {

	public static void main(String[] args) {
		
		
		//Sysntax for ASCII:-(int)'character'
		
		
		System.out.println("The Ascii value for A is "+(int)'A');
		
		System.out.println("The Ascii value for Z is "+(int)'Z');
		
		System.out.println("The Ascii value for a is "+(int)'a');
		
		System.out.println("The Ascii value for Z is "+(int)'z');
		
		System.out.println("The Ascii value for 0 is "+(int)'0');
		
		System.out.println("The Ascii value for 9 is "+(int)'9');
		
		
		//Sysntax for characters:-(char)numeric value
		
		System.out.println("The character value for 65 is "+(char)65);
		
	}

}
