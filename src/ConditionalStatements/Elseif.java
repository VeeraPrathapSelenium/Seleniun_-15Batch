package ConditionalStatements;

public class Elseif {

	public static void main(String[] args) {

int a=97;

int b=109;



if(a>b)
{
	
	System.out.println("A is greater than B");
	
}

else if(a>=60 && a<=90)
{
	
	System.out.println("A is less than B but it is in the range of 60 and 90");
	
	
}
else
{
	System.out.println("A is less than B and it is not in the range of 60 and 90");
	
}
		
		
		

	}

}
